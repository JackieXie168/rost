/* Callback declarations */
static int system_hostname_commit(clicon_handle h, char *, trans_cb_type, lv_op_t, char *, void *);

/* Map of key-callback pairs for system */
struct {
    char *key;
    trans_cb cb;
} system_depmap[] = {
    {"system.hostname", system_hostname_commit},

    {NULL, NULL},
};


/*
 * Plugin initialization
 */
int
plugin_init(clicon_handle h)
{
    int i;
    char *key;
    int retval = -1;

    for (i = 0; system_depmap[i].key != NULL; i++) {
	key = system_depmap[i].key;
	if (dbdep(h, TRANS_CB_COMMIT, system_depmap[i].cb, (void *)NULL, 1, key) == NULL){
	    clicon_debug(1, "Failed to create dependency '%s'", key);
	    goto done;
	}
	clicon_debug(1, "Created dependency '%s'", key);
    }	
    retval = 0;
  done:
    return retval;
}

/*
 * Plugin start
 */
int
plugin_start(clicon_handle h, int argc, char **argv)
{
    return 0;
}

/*
 * Reset system state
 */
int
plugin_reset(clicon_handle h)
{
    char hostname[HOST_NAME_MAX];
    char *fmt;
    char *db = clicon_running_db(h);
    int retval = -1;

    if (gethostname(hostname, HOST_NAME_MAX) < 0)
	snprintf(hostname, HOST_NAME_MAX-1, "osr");
    fmt = chunk_sprintf(__FUNCTION__, 
			"system.hostname $hostname=\"%s\"", 
			hostname);
    /* (1) Default hostname*/
    if (db_lv_op(clicon_dbspec_key(h), db, LV_SET, fmt, NULL) < 0)
	goto done;
    retval = 0;
  done:
    unchunk_group(__FUNCTION__);
    return retval;
}


/*
 * Hostname commit callback. 
 */
static int
system_hostname_commit(clicon_handle h, char *db,
		       trans_cb_type tt, 
		       lv_op_t op,
		       char *key,
		       void *arg)
{
    int ret;
    cg_var *cgv;
    char *hostname;
    
    /* Get configured hostname from db */
    if (op == LV_SET)
	cgv = dbvar2cv (db, "system.hostname", "hostname");
    else
	cgv = NULL;

    if (cgv == NULL || !strlen(cv_string_get(cgv)))
	hostname = "Router";
    else
	hostname = cv_string_get(cgv);

    if ((ret = sethostname(hostname, strlen(hostname))) != 0) 
	clicon_err(OE_UNIX, errno, "%s: sethostname", __FUNCTION__);
    
    if(cgv) 
	cv_free(cgv);

    return ret;
}

